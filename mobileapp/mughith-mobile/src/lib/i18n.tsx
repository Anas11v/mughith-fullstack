export function tpl(s: string, vars?: Record<string, string | number>): string {
  if (!s) return s;
  return s.replace(/\{(\w+)\}/g, (_, k) => (vars && vars[k] != null ? String(vars[k]) : ''));
}

export function arDigits(s: string | number): string {
  const map = ['٠', '١', '٢', '٣', '٤', '٥', '٦', '٧', '٨', '٩'];
  return String(s).replace(/\d/g, (d) => map[+d]);
}

interface I18nValue {
  lang: Lang;
  setLang: (l: Lang) => void;
  t: (key: string) => string;
  tpl: (key: string, vars?: Record<string, string | number>) => string;
  num: (n: number | string) => string;
  dir: 'ltr' | 'rtl';
  isAr: boolean;
}

const I18nCtx = createContext<I18nValue>({
  lang: 'en',
  setLang: () => {},
  t: (k) => k,
  tpl: (k) => k,
  num: (n) => String(n),
  dir: 'ltr',
  isAr: false,
});

const STORAGE_KEY = 'mughith_lang';

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>('en');

  useEffect(() => {
    (async () => {
      const stored = await AsyncStorage.getItem(STORAGE_KEY);
      if (stored === 'ar' || stored === 'en') setLangState(stored);
    })();
  }, []);

  useEffect(() => {
    const isRTL = lang === 'ar';
    if (I18nManager.isRTL !== isRTL) {
      I18nManager.allowRTL(isRTL);
      I18nManager.forceRTL(isRTL);
    }
    AsyncStorage.setItem(STORAGE_KEY, lang);
  }, [lang]);

  const setLang = useCallback((l: Lang) => setLangState(l), []);

  const t = useCallback(
    (k: string): string => {
      const dict = STRINGS[lang];
      return dict[k] ?? STRINGS.en[k] ?? k;
    },
    [lang],
  );

  const tplFn = useCallback(
    (k: string, vars?: Record<string, string | number>) => tpl(t(k), vars),
    [t],
  );

  const num = useCallback(
    (n: number | string) => (lang === 'ar' ? arDigits(n) : String(n)),
    [lang],
  );

  const value = useMemo<I18nValue>(
    () => ({
      lang,
      setLang,
      t,
      tpl: tplFn,
      num,
      dir: lang === 'ar' ? 'rtl' : 'ltr',
      isAr: lang === 'ar',
    }),
    [lang, setLang, t, tplFn, num],
  );

  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>;
}

export function useI18n(): I18nValue {
  return useContext(I18nCtx);
}
